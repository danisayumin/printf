#include	"ft_printf.h"

void	ft_putnbr(int n)
{
	long int	i;

	i = n;
	if (i < 0)
	{
		ft_putchar_fd('-');
		i = -i;
	}
	if (i >= 10)
		ft_putnbr(i / 10);
	ft_putchar_fd(i % 10 + '0');
}
