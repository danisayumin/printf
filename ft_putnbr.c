#include	"printf.h"

int	ft_putnbr(int n)
{
	long int	i;
	int			j;

	j = 0;
	i = n;
	if (i < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i >= 10)
		j += ft_putnbr(i / 10);
	ft_putchar(i % 10 + '0');
	return (i);
}
